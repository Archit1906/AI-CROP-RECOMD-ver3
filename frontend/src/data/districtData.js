// ── HELPER FUNCTIONS ─────────────────────────────────────────
export const getDistrictZone = (districtName) => {
  return DISTRICT_ZONES[districtName] || 'plain'
}

export const getSoilForDistrict = (stateName, districtName) => {
  const base     = SOIL_DATA[stateName] || SOIL_DATA["Tamil Nadu"]
  const zone     = getDistrictZone(districtName)
  const modifier = ZONE_MODIFIERS[zone] || ZONE_MODIFIERS.plain

  return {
    N:        Math.max(20, Math.round(base.N  + modifier.N)),
    P:        Math.max(5,  Math.round(base.P  + modifier.P)),
    K:        Math.max(5,  Math.round(base.K  + modifier.K)),
    ph:       Math.max(4,  Math.min(9.5, parseFloat((base.ph + modifier.ph).toFixed(1)))),
    zone,
    moisture: base.moisture
  }
}

export const getWeatherForDistrict = (stateName, districtName) => {
  const base     = STATE_WEATHER[stateName] || STATE_WEATHER["Tamil Nadu"]
  const zone     = getDistrictZone(districtName)
  const modifier = ZONE_MODIFIERS[zone] || ZONE_MODIFIERS.plain

  return {
    temperature: Math.round(base.temperature + (modifier.temp || 0)),
    humidity:    Math.max(10, Math.min(99, Math.round(base.humidity + modifier.humidity))),
    rainfall:    Math.max(10, Math.round(base.rainfall + modifier.rainfall))
  }
}

